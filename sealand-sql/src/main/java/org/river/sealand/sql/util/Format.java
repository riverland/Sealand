package org.river.sealand.sql.util;

/**
 * <p>
 * sql 格式化
 * 
 * @author river
 * @since Nov 16, 2013
 */
public class Format {

	/** 制表符占位长度 */
	public final static int TabLen = 8;

	/** 制表符 */
	public final static byte TAB = 0x8;

	/** 换行 */
	public final static byte LF = 0xA;

	/**换页符*/
	public final static byte FF = 0xC;

	/**回车*/
	public final static byte CR = 0xD;

	/**传输结束*/
	public final static byte EOT = 0x4;	
	
	

}
